import type { JSONSchema7 } from "json-schema";
import type { DeepPartial, TypeAtPath, TypePath } from "./type-path-utils";
import type { AsyncIterableStream } from "../../utils";
import type { StandardSchemaV1 } from "@standard-schema/spec";
import type { ToolResponse } from "./ToolResponse";

export type ToolModelContentPart =
  | {
      /** A text content part returned to the model after a tool call. */
      readonly type: "text";
      /** Text that should be included in the model-visible tool result. */
      readonly text: string;
    }
  | {
      /** A file content part returned to the model after a tool call. */
      readonly type: "file";
      /**
       * File payload encoded as a provider-compatible string, commonly base64
       * for binary data.
       */
      readonly data: string;
      /** MIME type for the file payload. */
      readonly mediaType: string;
      /** Optional display filename for the file payload. */
      readonly filename?: string;
    };

/**
 * Converts a tool's runtime result into content that is sent back to the
 * model.
 *
 * Return this when the value shown in the UI or stored as the tool result
 * should differ from the model-visible response. When omitted, the successful
 * tool result is sent back to the model as-is. If a tool returns a
 * `ToolResponse` with `modelContent`, that explicit content is used instead
 * of calling this function.
 *
 * @example
 * ```ts
 * const toModelOutput: ToolModelOutputFunction<
 *   { documentId: string },
 *   { summary: string; pdfBase64: string }
 * > = ({ output }) => [
 *   { type: "text", text: output.summary },
 *   {
 *     type: "file",
 *     data: output.pdfBase64,
 *     mediaType: "application/pdf",
 *   },
 * ];
 * ```
 */
export type ToolModelOutputFunction<TArgs, TResult> = (options: {
  /** Stable identifier for the tool call being converted. */
  toolCallId: string;
  /** Arguments supplied by the model for this tool call. */
  input: TArgs;
  /** Value returned by the tool's {@link ToolExecuteFunction}. */
  output: TResult;
}) =>
  | readonly ToolModelContentPart[]
  | Promise<readonly ToolModelContentPart[]>;

/**
 * Interface for reading tool call arguments from a stream, which are
 * generated by a language learning model (LLM). Provides methods to
 * retrieve specific values, partial streams, or complete items from
 * an array, based on a specified path.
 *
 * @template TArgs The type of arguments being read.
 */
export interface ToolCallArgsReader<TArgs extends Record<string, unknown>> {
  /**
   * Returns a promise that will resolve to the value at the given path,
   * as soon as that path is generated by the LLM.
   *
   * @param fieldPath An array of object keys or array indices.
   */
  get<PathT extends TypePath<TArgs>>(
    ...fieldPath: PathT
  ): Promise<TypeAtPath<TArgs, PathT>>;

  /**
   * Returns a stream that will emit partial values at the given path,
   * as they are generated by the LLM.
   *
   * @param fieldPath An array of object keys or array indices.
   */
  streamValues<PathT extends TypePath<TArgs>>(
    ...fieldPath: PathT
  ): AsyncIterableStream<DeepPartial<TypeAtPath<TArgs, PathT>>>;

  /**
   * Returns a stream that will emit partial text at the given path,
   * as they are generated by the LLM.
   *
   * @param fieldPath An array of object keys or array indices.
   */
  streamText<PathT extends TypePath<TArgs>>(
    ...fieldPath: PathT
  ): TypeAtPath<TArgs, PathT> extends string & (infer U)
    ? AsyncIterableStream<U>
    : never;

  /**
   * Returns a stream that will emit complete items in the array
   * at the given path, as they are generated by the LLM.
   *
   * @param fieldPath An array of object keys or array indices.
   */
  forEach<PathT extends TypePath<TArgs>>(
    ...fieldPath: PathT
  ): TypeAtPath<TArgs, PathT> extends Array<infer U>
    ? AsyncIterableStream<U>
    : never;
}

export interface ToolCallResponseReader<TResult> {
  get: () => Promise<ToolResponse<TResult>>;
}

export interface ToolCallReader<
  TArgs extends Record<string, unknown> = Record<string, unknown>,
  TResult = unknown,
> {
  args: ToolCallArgsReader<TArgs>;
  response: ToolCallResponseReader<TResult>;

  /**
   * @deprecated Use {@link ToolCallReader.response} and read
   * `response.get().result` instead.
   */
  result: {
    get: () => Promise<TResult>;
  };
}

export type ToolExecutionContext = {
  /** Stable identifier for the tool call being executed. */
  toolCallId: string;
  /** Signal that is aborted when the current run is cancelled. */
  abortSignal: AbortSignal;
  /**
   * From inside a frontend tool's execute function, request human input from
   * the UI. Resolves with the payload the UI supplies.
   */
  human: (payload: unknown) => Promise<unknown>;
};

/**
 * Function called when assistant-ui executes a frontend tool.
 */
export type ToolExecuteFunction<TArgs, TResult> = (
  args: TArgs,
  context: ToolExecutionContext,
) => TResult | Promise<TResult>;

export type ToolStreamCallFunction<
  TArgs extends Record<string, unknown> = Record<string, unknown>,
  TResult = unknown,
> = (
  reader: ToolCallReader<TArgs, TResult>,
  context: ToolExecutionContext,
) => void;

type OnSchemaValidationErrorFunction<TResult> = ToolExecuteFunction<
  unknown,
  TResult
>;

/**
 * Per-provider metadata forwarded into the wire request body verbatim.
 * assistant-ui does not interpret these values; downstream adapters (AI SDK,
 * custom routes) pass them to the model provider as-is.
 *
 * The outer key is the provider name (`anthropic`, `openai`, ...); the inner
 * object is whatever shape that provider's SDK expects under
 * `tool.providerOptions[providerName]`. Use this to enable provider-specific
 * tool behaviors such as Anthropic's `defer_loading`
 * (`{ anthropic: { deferLoading: true } }`) without adding provider-aware
 * code in assistant-ui.
 */
export type ProviderOptions = Record<string, Record<string, unknown>>;

/**
 * Controls how a tool call's UI is presented relative to the assistant's
 * chain-of-thought trace.
 *
 * - `"inline"` — the tool call is part of the reasoning trace and is folded
 *   into the chain-of-thought grouping alongside other routine tool calls.
 * - `"standalone"` — the tool call is surfaced on its own, outside the
 *   chain-of-thought grouping (e.g. a human-in-the-loop prompt, a generative
 *   UI surface, or an important action UI worth showing prominently).
 *
 * This is a client-side presentation hint only; it does not affect how the
 * tool is exposed to or executed by the model.
 */
type ToolDisplay = "standalone" | "inline";

type ToolBase<
  TArgs extends Record<string, unknown> = Record<string, unknown>,
  TResult = unknown,
> = {
  /**
   * @deprecated Experimental, API may change.
   */
  streamCall?: ToolStreamCallFunction<TArgs, TResult>;

  /**
   * How this tool's UI is presented relative to the chain-of-thought trace.
   *
   * Defaults to `"inline"` (folded into the chain-of-thought grouping).
   * Set `"standalone"` to surface the tool call on its own. `human` tools are
   * always `"standalone"` and cannot opt out.
   *
   * @see ToolDisplay
   */
  display?: ToolDisplay;

  /**
   * @internal Defaults already known by the backend for this tool. Client
   * transports omit matching fields and only upload overrides.
   *
   * This is only meaningful for frontend and human tools.
   */
  unstable_backendDefault?: {
    parameters?: boolean;
  };
};

type BackendTool<
  TArgs extends Record<string, unknown> = Record<string, unknown>,
  TResult = unknown,
> = ToolBase<TArgs, TResult> & {
  /** Tool that is executed by the backend rather than in the browser. */
  type: "backend";

  description?: undefined;
  parameters?: undefined;
  disabled?: undefined;
  execute?: undefined;
  toModelOutput?: undefined;
  experimental_onSchemaValidationError?: undefined;
  providerOptions?: undefined;
};

/**
 * Backend tool as *authored* (a {@link ToolDeclaration}), before the build
 * splits it: it may declare a `description`, `parameters`, and a server-side
 * `execute`. The canonical {@link BackendTool} keeps these `undefined` because,
 * once split, the client never sees them and the server consumes them through a
 * server adapter rather than the shared {@link Tool} shape.
 */
type BackendToolDeclaration<
  TArgs extends Record<string, unknown> = Record<string, unknown>,
  TResult = unknown,
> = ToolBase<TArgs, TResult> & {
  type: "backend";

  /** Natural-language description shown to the model when selecting tools. */
  description?: string | undefined;
  /** Schema for the arguments the model must provide when calling the tool. */
  parameters?: StandardSchemaV1<TArgs> | JSONSchema7 | undefined;
  /** Prevents the tool from being exposed to the model while true. */
  disabled?: boolean;
  /** Executes the tool on the server after the model provides valid arguments. */
  execute?: ToolExecuteFunction<TArgs, TResult>;
  /** Converts the execution result into model-visible output. */
  toModelOutput?: ToolModelOutputFunction<TArgs, TResult>;
  /** Handles invalid tool arguments when schema validation fails. */
  experimental_onSchemaValidationError?: OnSchemaValidationErrorFunction<TResult>;
  providerOptions?: ProviderOptions;
};

type FrontendTool<
  TArgs extends Record<string, unknown> = Record<string, unknown>,
  TResult = unknown,
> = ToolBase<TArgs, TResult> & {
  /** Tool that is executed in the frontend runtime. */
  type: "frontend";

  /** Natural-language description shown to the model when selecting tools. */
  description?: string | undefined;
  /** Schema for the arguments the model must provide when calling the tool. */
  parameters: StandardSchemaV1<TArgs> | JSONSchema7;
  /** Prevents the tool from being exposed to the model while true. */
  disabled?: boolean;
  /** Executes the tool after the model provides valid arguments. */
  execute: ToolExecuteFunction<TArgs, TResult>;
  /** Converts the execution result into model-visible output. */
  toModelOutput?: ToolModelOutputFunction<TArgs, TResult>;
  /** Handles invalid tool arguments when schema validation fails. */
  experimental_onSchemaValidationError?: OnSchemaValidationErrorFunction<TResult>;
  providerOptions?: ProviderOptions;
};

type HumanTool<
  TArgs extends Record<string, unknown> = Record<string, unknown>,
  TResult = unknown,
> = ToolBase<TArgs, TResult> & {
  /** Tool that pauses the run until a user or UI supplies a result. */
  type: "human";

  /** Natural-language description shown to the model when selecting tools. */
  description?: string | undefined;
  /** Schema for the arguments the model must provide when requesting input. */
  parameters: StandardSchemaV1<TArgs> | JSONSchema7;
  /** Prevents the tool from being exposed to the model while true. */
  disabled?: boolean;
  /** Human tools are always surfaced standalone and cannot opt out. */
  display?: "standalone";
  execute?: undefined;
  toModelOutput?: undefined;
  experimental_onSchemaValidationError?: undefined;
  providerOptions?: ProviderOptions;
};

type ProviderTool<
  TArgs extends Record<string, unknown> = Record<string, unknown>,
  TResult = unknown,
> = ToolBase<TArgs, TResult> & {
  /** Tool executed by the LLM provider rather than assistant-ui. */
  type: "provider";

  /** Provider-defined tool identifier, e.g. `openai.web_search_preview`. */
  providerId: `${string}.${string}`;
  /** Schema used by adapters for validation and type plumbing. */
  parameters?: StandardSchemaV1<TArgs> | JSONSchema7 | undefined;
  /** Provider-specific configuration for this tool. */
  args: Record<string, unknown>;
  /**
   * Whether provider results may arrive after the originating response turn.
   */
  supportsDeferredResults?: boolean;

  description?: undefined;
  disabled?: boolean;
  execute?: undefined;
  toModelOutput?: undefined;
  experimental_onSchemaValidationError?: undefined;
  providerOptions?: ProviderOptions;
};

export type McpServerConfig =
  | {
      /** Connect to an MCP server over Streamable HTTP or server-sent events. */
      type: "http" | "sse";
      url: string;
      headers?: Record<string, string>;
      redirect?: "follow" | "error";
    }
  | {
      /** Start and connect to a local MCP server over stdio. */
      type: "stdio";
      command: string;
      args?: readonly string[];
      env?: Record<string, string>;
      cwd?: string;
    };

type McpTool = ToolBase<Record<string, unknown>, unknown> & {
  /** Tools loaded from an MCP server by a server adapter. */
  type: "mcp";
  server: McpServerConfig;

  description?: undefined;
  parameters?: undefined;
  disabled?: boolean;
  execute?: undefined;
  toModelOutput?: undefined;
  experimental_onSchemaValidationError?: undefined;
  providerOptions?: undefined;
};

/**
 * Definition for a tool that can be exposed to the assistant model.
 *
 * Use `type: "frontend"` for tools executed in the browser, `type: "backend"`
 * for tools handled by your server, and `type: "human"` for flows that pause
 * until the UI supplies a result.
 *
 * @example
 * ```ts
 * const frontendTool: Tool<{ city: string }, string> = {
 *   type: "frontend",
 *   description: "Get the weather for a city.",
 *   parameters: {
 *     type: "object",
 *     properties: { city: { type: "string" } },
 *     required: ["city"],
 *   },
 *   execute: async ({ city }) => `Sunny in ${city}`,
 * };
 * ```
 *
 * @example
 * ```ts
 * const backendTool: Tool = {
 *   type: "backend",
 * };
 * ```
 *
 * @example
 * ```ts
 * const humanTool: Tool<{ question: string }, string> = {
 *   type: "human",
 *   description: "Ask the user a follow-up question.",
 *   parameters: {
 *     type: "object",
 *     properties: { question: { type: "string" } },
 *     required: ["question"],
 *   },
 * };
 * ```
 */
export type Tool<
  TArgs extends Record<string, unknown> = Record<string, unknown>,
  TResult = unknown,
> =
  | FrontendTool<TArgs, TResult>
  | BackendTool<TArgs, TResult>
  | HumanTool<TArgs, TResult>
  | ProviderTool<TArgs, TResult>
  | McpTool
  | ToolWithoutType<TArgs, TResult>;

/**
 * A tool as *authored* — the permissive counterpart to {@link Tool}. Unlike
 * {@link Tool}, a `backend` entry may declare `description`, `parameters`, and a
 * server-side `execute`. Use this for the input of authoring helpers (e.g.
 * `defineToolkit`); the canonical {@link Tool} is the output.
 */
export type ToolDeclaration<
  TArgs extends Record<string, unknown> = Record<string, unknown>,
  TResult = unknown,
> =
  | FrontendTool<TArgs, TResult>
  | BackendToolDeclaration<TArgs, TResult>
  | HumanTool<TArgs, TResult>
  | ProviderTool<TArgs, TResult>
  | McpTool
  | ToolWithoutType<TArgs, TResult>;

/**
 * @deprecated Use {@link Tool} with an explicit `type` field instead.
 */
export type ToolWithoutType<
  TArgs extends Record<string, unknown> = Record<string, unknown>,
  TResult = unknown,
> = (
  | Omit<FrontendTool<TArgs, TResult>, "type">
  | Omit<BackendTool<TArgs, TResult>, "type">
  | Omit<HumanTool<TArgs, TResult>, "type">
  | Omit<ProviderTool<TArgs, TResult>, "type">
) & { type?: undefined };
